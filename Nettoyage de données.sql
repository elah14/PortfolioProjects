-- Supprimer les doublons
-- Utilisation d'une CTE et de ROW_NUMBER pour identifier les lignes identiques

WITH RowNumCTE AS(
Select *,
	ROW_NUMBER() OVER (
	PARTITION BY ParcelID,
				 PropertyAddress,
				 SalePrice,
				 SaleDate,
				 LegalReference
				 ORDER BY
					UniqueID
					) row_num
From PortfolioProject.dbo.NashvilleHousing
)
-- On sélectionne ici les doublons (row_num > 1). Remplacer "Select *" par "DELETE" pour les supprimer.
Select *
From RowNumCTE
Where row_num > 1
Order by PropertyAddress

